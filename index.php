<script>
    // Video data array
    const videos = [
        { id: 1, title: "Campus Tour", src: "assets/videos/xjmu1.mp4" },
        { id: 2, title: "Student Life", src: "assets/videos/xjmu2.mp4" },
        { id: 3, title: "Research Facilities", src: "assets/videos/xjmu3.mp4" }
    ];

    // Get gallery and modal containers
    const galleryContainer = document.getElementById("videoGallery");
    const modalContainer = document.getElementById("videoModals");

    // Generate gallery and modals dynamically
    videos.forEach(video => {
        // Add video preview to gallery
        galleryContainer.innerHTML += `
            <div class="col-md-4">
                <div class="video-item" data-bs-toggle="modal" data-bs-target="#videoModal${video.id}">
                    <video width="100%" height="200" class="rounded shadow" muted>
                        <source src="${video.src}" type="video/mp4">
                        Your browser does not support the video tag.
                    </video>
                </div>
            </div>
        `;

        // Add corresponding modal
        modalContainer.innerHTML += `
            <div class="modal fade" id="videoModal${video.id}" tabindex="-1" aria-hidden="true">
                <div class="modal-dialog modal-lg">
                    <div class="modal-content">
                        <div class="modal-header">
                            <h5 class="modal-title">${video.title}</h5>
                            <button type="button" class="btn-close" data-bs-dismiss="modal"></button>
                        </div>
                        <div class="modal-body">
                            <video id="video${video.id}" width="100%" height="400" controls>
                                <source src="${video.src}" type="video/mp4">
                            </video>
                        </div>
                    </div>
                </div>
            </div>
        `;
    });

    // JavaScript to Stop Video on Modal Close
    document.addEventListener("DOMContentLoaded", function() {
        document.querySelectorAll(".modal").forEach(modal => {
            modal.addEventListener("hidden.bs.modal", function() {
                let video = modal.querySelector("video");
                if (video) {
                    video.pause();
                    video.currentTime = 0;
                }
            });
        });
    });
</script>


